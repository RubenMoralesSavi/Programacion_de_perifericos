#include <stdio.h>
#include <iostream>
#include <pch.h>

int Rectangulo::Rectangulo(double h, double l) {
	class Rectangulo {
		double altura_;
		double ancho_;
	public :
		Rectangulo(double, double);
		double Area();
	}
	ancho_ = l;
	alto_ = h;

double Rectangulo::Area();
return alto * ancho;
}

int main() {
	Rectangulo figura(3, 4);
	cout << "El area es : " << figura.area() << endl;
	Rectangulo otrafig();
	cout << "El area es : " << otrafig.area() << endl;
}
